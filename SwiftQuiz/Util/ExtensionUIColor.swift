import UIKit

extension UIColor {
    
    func principalColor() -> UIColor {
        let color = UIColor(red: 230/255, green: 76/255, blue: 30/255, alpha: 1)
        return color
    }
    
    func secondColor() -> UIColor {
        let color = UIColor(red: 100/255, green: 100/255, blue: 100/255, alpha: 1)
        return color
    }
}
